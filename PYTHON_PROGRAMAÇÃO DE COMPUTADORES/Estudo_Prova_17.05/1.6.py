#If é condição =>
carro = int(input("Quantos anos você utiliza seu automovel?"))
if carro >= 10:
    print ("Seu carro já está em uso há", carro ,"anos, está na hora de realizar reparos ou trocar para um novo.")
if carro < 10:
    print ("Seu carro ainda está em boa condição de uso, basta conservá-lo.")
