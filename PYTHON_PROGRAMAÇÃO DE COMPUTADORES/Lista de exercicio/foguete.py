#Faça um programa para escrever a contagem regressiva do
#lançamento de um foguete.
#O programa deve imprimir 10, 9, 8, ..., 1, 0 e Fogo! na tela


x = 10 #armazena a variavel
while x >= 0: #quando x for maior ou igual a 0
    print(x) #mostre na tela o valor de x
    x = x - 1 #aqui onde ocorre a contagem regrassiva
print("Fogo!") #mostre na tela após o processo de contagem: Fogo
